export function getMockData(endpoint: string, options?: RequestInit): any {
  const path = endpoint.split('?')[0];

  if (path === '/products') return { data: { products: mockProducts, pagination: { total_count: mockProducts.length, page: 1, total_pages: 1 } } };
  if (path.startsWith('/products/search')) {
    const q = new URLSearchParams(endpoint.split('?')[1]).get('q')?.toLowerCase() || '';
    const results = mockProducts.filter(p => p.name.toLowerCase().includes(q) || p.description.toLowerCase().includes(q));
    return { data: { products: results.slice(0, 6) } };
  }
  if (path.match(/^\/products\/[\w-]+\/related$/)) {
    const slug = path.split('/')[2];
    const product = mockProducts.find(p => p.slug === slug);
    const related = product ? mockProducts.filter(p => p.category.slug === product.category.slug && p.slug !== slug).slice(0, 4) : [];
    return { data: { products: related } };
  }
  if (path.match(/^\/products\/[\w-]+$/)) {
    const slug = path.split('/')[2];
    return { data: { product: mockProducts.find(p => p.slug === slug) || null } };
  }
  if (path === '/collections') return { data: mockCollections };
  if (path.match(/^\/collections\/[\w-]+\/products$/)) {
    const slug = path.split('/')[2];
    let filtered = mockProducts;
    if (slug === 'for-him') filtered = mockProducts.filter(p => p.tags.includes('him'));
    else if (slug === 'for-her') filtered = mockProducts.filter(p => p.tags.includes('her'));
    else filtered = mockProducts.filter(p => p.category.slug === slug);
    return { data: { products: filtered, pagination: { total_count: filtered.length } } };
  }
  if (path === '/settings') return { data: { social: { instagram: 'https://instagram.com/mancinimilano', tiktok: null, facebook: null }, store_name: 'Mancini Milano' } };
  if (path === '/cart' && options?.method === 'POST') return { data: { id: 'mock-cart-' + Date.now(), items: [], subtotal: 0, total: 0 } };

  return { data: null };
}
